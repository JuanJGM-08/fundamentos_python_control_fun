contador = 1
while contador <= 5:
    print(contador)
    contador += 1

# En este ejemplo, se utiliza un bucle 'while' para contar desde 1 hasta 5. La variable 'contador' se inicializa en 1 y se incrementa en cada iteración del bucle hasta que alcanza el valor de 5. En cada iteración, se imprime el valor actual de 'contador'.