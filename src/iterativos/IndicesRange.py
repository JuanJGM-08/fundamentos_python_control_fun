nombres = ["Ana", "Carlos", "Elena"]
for i in range(len(nombres)):
    print(f"Posición {i}: {nombres[i]}")
# El programa define una lista de nombres y luego utiliza un bucle 'for' junto con la función 'range()' para iterar sobre los índices de la lista. En cada iteración, se imprime la posición del índice y el nombre correspondiente en esa posición.