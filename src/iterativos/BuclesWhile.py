    # Cuerpo del bucle
#else:
    # Código que se ejecuta si el bucle termina normalmente

# En este ejemplo, se muestra la estructura de un bucle 'while' con una cláusula 'else'. El bloque de código dentro del 'else' se ejecutará solo si el bucle 'while' termina normalmente, es decir, sin ser interrumpido por una declaración 'break'. Si el bucle se interrumpe con 'break', el bloque 'else' no se ejecutará. Esto puede ser útil para realizar acciones adicionales después de que el bucle haya completado su iteración sin interrupciones.