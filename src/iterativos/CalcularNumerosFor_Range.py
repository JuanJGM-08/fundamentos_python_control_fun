n = 10
suma = 0
for i in range(1, n+1):
    suma += i
print(f"La suma de los primeros {n} números es: {suma}")  # 55
# En este ejemplo, se utiliza un bucle 'for' junto con la función 'range()' para iterar sobre los números del 1 al 10. En cada iteración, el número actual 'i' se suma a la variable 'suma'. Al finalizar el bucle, se imprime el resultado de la suma de los primeros 10 números.