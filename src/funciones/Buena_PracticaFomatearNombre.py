def formatear_nombre(nombre, apellido):
    """
    Formatea un nombre completo en formato "Apellido, Nombre".

    Args:
        nombre: Nombre de la persona
        apellido: Apellido de la persona

    Returns:
        Cadena formateada como "Apellido, Nombre"

    Ejemplo:
        >>> formatear_nombre("Juan", "Pérez")
        'Pérez, Juan'
    """
    return f"{apellido}, {nombre}"

# este código define una función `formatear_nombre` que toma dos argumentos, `nombre` y `apellido`, y devuelve una cadena formateada en el formato "Apellido, Nombre". La función incluye una docstring que describe su propósito, los argumentos que recibe, lo que devuelve y un ejemplo de uso. Al llamar a esta función con diferentes nombres y apellidos, se puede obtener el formato deseado para cada caso. Este ejemplo muestra cómo crear una función para formatear nombres y cómo documentarla adecuadamente para mejorar su claridad y usabilidad.
