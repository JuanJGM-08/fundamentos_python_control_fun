def sumar(a, b):
    """Suma dos números y devuelve el resultado."""
    return a + b

#este código define una función `sumar` que toma dos parámetros, `a` y `b`, y devuelve la suma de ambos. La función incluye una docstring que describe su propósito. Al llamar a la función con los argumentos 5 y 3, se obtiene el resultado 8, que se imprime en la consola. Este ejemplo muestra cómo crear una función simple para realizar una operación matemática básica y cómo documentarla adecuadamente.