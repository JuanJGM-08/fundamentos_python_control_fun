def saludar_persona(nombre):  # 'nombre' es un parámetro
    print(f"Hola, {nombre}!")

saludar_persona("Ana")  # "Ana" es un argumento
# En este código, la función `saludar_persona` toma un parámetro llamado `nombre`. Al llamar a la función, se le pasa el argumento "Ana", lo que hace que la función imprima "Hola, Ana!". Este ejemplo muestra cómo los parámetros y argumentos funcionan en las funciones de Python.