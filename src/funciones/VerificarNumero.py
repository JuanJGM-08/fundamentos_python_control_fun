# Función que verifica si un número es par
def es_par(numero):
    return numero % 2 == 0

# Función que convierte temperatura de Celsius a Fahrenheit
def celsius_a_fahrenheit(celsius):
    return (celsius * 9/5) + 32
# este código define dos funciones: `es_par` para verificar si un número es par y `celsius_a_fahrenheit` para convertir temperaturas de Celsius a Fahrenheit. 