def calcular_cuadrado(numero):
    resultado = numero * numero
    return resultado  # Devuelve el valor y termina la función

area = calcular_cuadrado(4)
print(area)  # Imprime: 16

# En este código, la función `calcular_cuadrado` toma un número como argumento, calcula su cuadrado y devuelve el resultado. Al llamar a la función con el número 4, se obtiene el resultado 16, que se imprime en la consola. Este ejemplo muestra cómo una función puede devolver un valor que luego puede ser utilizado en otras partes del programa.