edad = 17
mensaje = "Eres mayor de edad." if edad >= 18 else "Eres menor de edad."
print(mensaje)

# El programa utiliza una expresión ternaria para evaluar la variable 'edad' y asignar un mensaje correspondiente a la variable 'mensaje'. Si la edad es mayor o igual a 18, se asigna "Eres mayor de edad.", de lo contrario, se asigna "Eres menor de edad.". Finalmente, se imprime el mensaje resultante.