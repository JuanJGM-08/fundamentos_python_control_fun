a = 1

b = 2

print("El máximo es:", a if a > b else b)

# El programa utiliza una expresión ternaria para comparar las variables 'a' y 'b' y determinar cuál de ellas es mayor. La expresión 'a if a > b else b' evalúa si 'a' es mayor que 'b'; si es así, devuelve 'a', de lo contrario, devuelve 'b'. Finalmente, se imprime el resultado indicando cuál es el máximo entre 'a' y 'b'.