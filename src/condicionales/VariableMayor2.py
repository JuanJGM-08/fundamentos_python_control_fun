a = 5
b = 10
c = 15
mayor = a

if b > mayor:
    mayor = b

if c > mayor:
    mayor = c

print(f'El número mayor es {mayor}.')

# El programa evalúa tres variables 'a', 'b' y 'c' para determinar cuál de ellas es la mayor. Inicialmente, se asigna el valor de 'a' a la variable 'mayor'. Luego, se compara 'b' con 'mayor' y si 'b' es mayor, se actualiza el valor de 'mayor' a 'b'. Después, se compara 'c' con 'mayor' y si 'c' es mayor, se actualiza el valor de 'mayor' a 'c'. Finalmente, se imprime el número mayor encontrado.